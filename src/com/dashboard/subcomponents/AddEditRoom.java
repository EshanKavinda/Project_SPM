    private void addRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomBtnActionPerformed
        // TODO add your handling code here:

        Room room = new Room();
        room.setRoomId(room_id);
        room.setBuildingName(buildingName_jComboBox.getSelectedItem().toString());
        room.setRoomName(roomname.getText().toString());
        room.setCapacity(capacityroom.getText().toString());
        room.setType(roomtype_jComboBox.getSelectedItem().toString());
        
        RoomService roomService = new RoomService();
        if (addRoomBtn.getText().toLowerCase().contains("add")) {
            roomService.addRoom(room);
        }else{
            if (room_id == 0) {
                System.out.println("Update error");
            }else{
                roomService.updateRoom(room);
            }
        }
        
        int i = JOptionPane.showConfirmDialog(this, "Do you want to view all room details" ,"Success", JOptionPane.YES_NO_OPTION);
            if(i==0){
                jTabbedPane.remove(0);
                jTabbedPane.add("View Rooms", new ViewRoom(jTabbedPane));
            }
        
        
    }//GEN-LAST:event_addRoomBtnActionPerformed


