    public AddEditRoom(Room room, JTabbedPane jTabbedPane) {
        initComponents();
        this.jTabbedPane = jTabbedPane;
    
        BuildingService bs = new BuildingService();
        ResultSet resultSet = bs.getAllBuildings();
        
        try {
            buildingName_jComboBox.addItem("-Select-");
            while (resultSet.next()) {
                buildingName_jComboBox.addItem(resultSet.getString("building_name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddEditRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (room.getRoomName() != null) {
            addRoomBtn.setText("Edit Room");
            room_id = room.getRoomId();
            buildingName_jComboBox.setSelectedItem(room.getBuildingName());
            roomname.setText(room.getRoomName());
            capacityroom.setText(room.getCapacity());
            roomtype_jComboBox.setSelectedItem(room.getType());
        }
        
    }

