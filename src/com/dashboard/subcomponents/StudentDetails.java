    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int row = jTable2.getSelectedRow();
        
        if (row >= 0) {
            String itNumber = jTable2.getValueAt(row, 0).toString();
            String name = jTable2.getValueAt(row, 1).toString().toUpperCase();

            int i = JOptionPane.showConfirmDialog(this, "Delete "+name ,"Confirm", JOptionPane.YES_NO_OPTION);
            if(i==0){
                StudentDService sService = new StudentDService();
                sService.deleteStudent(itNumber);
                JOptionPane.showMessageDialog(this, "Studnet delete successful");
            }
        //refresh table
            jTabbedPane.remove(0);
            jTabbedPane.add("Student Details", new StudentDetails(jTabbedPane));
        }else{
            JOptionPane.showMessageDialog(this, "Please select row!");
        }
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

