    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int row = viewLecturersTable.getSelectedRow();
        
        if (row >= 0) {
            int lecId = Integer.parseInt(viewLecturersTable.getValueAt(row, 0).toString());
            String lecname = viewLecturersTable.getValueAt(row, 1).toString();
            String empid = viewLecturersTable.getValueAt(row, 2).toString();
            String faculty = viewLecturersTable.getValueAt(row, 3).toString();
            String department = viewLecturersTable.getValueAt(row, 4).toString();
            String center = viewLecturersTable.getValueAt(row, 5).toString();
            String building = viewLecturersTable.getValueAt(row, 6).toString();
            String level = viewLecturersTable.getValueAt(row, 7).toString();
            String ranking = viewLecturersTable.getValueAt(row, 8).toString();

            Lecturer l = new Lecturer(lecId, lecname, empid, faculty, department, center, building, level, ranking);

            jTabbedPane.remove(0);
            jTabbedPane.add("Edit Lecturer Details", new AddLectures(l, jTabbedPane));
        }else{
            JOptionPane.showMessageDialog(this, "Please select row!");
        }
        
        
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        int row = viewLecturersTable.getSelectedRow();
        
        if (row >= 0) {
            int lecId = Integer.parseInt(viewLecturersTable.getValueAt(row, 0).toString());
            String lecname = viewLecturersTable.getValueAt(row, 1).toString().toUpperCase();

            int i = JOptionPane.showConfirmDialog(this, "Delete "+lecname ,"Confirm", JOptionPane.YES_NO_OPTION);
            if(i==0){
                LecturerService lecturerService = new LecturerService();
                lecturerService.deleteLecturer(lecId);
                JOptionPane.showMessageDialog(this, "Lecturer delete successful");
            }
        //refresh table
            jTabbedPane.remove(0);
            jTabbedPane.add("View Lecturers", new ViewLectures(jTabbedPane));
        }else{
            JOptionPane.showMessageDialog(this, "Please select row!");
        }
        
        
    }//GEN-LAST:event_deleteBtnActionPerformed


