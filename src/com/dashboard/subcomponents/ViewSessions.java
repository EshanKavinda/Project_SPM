    private void deleteSessionsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSessionsBtnActionPerformed
        // TODO add your handling code here:
        int row = sessions_jTable.getSelectedRow();

        if (row >= 0) {
            int sessionid = Integer.parseInt(sessions_jTable.getValueAt(row, 0).toString());

            int i = JOptionPane.showConfirmDialog(this, "Delete selected session" ,"Confirm", JOptionPane.YES_NO_OPTION);
            if(i==0){
                SessionService service = new SessionService();
                if (service.deleteSession(sessionid)) {
                    JOptionPane.showMessageDialog(this, "Session delete successful");
                }else{
                    JOptionPane.showMessageDialog(this, "Building delete failed");
                }
                
            }
            //refresh table
            jTabbedPane.remove(0);
            jTabbedPane.add("View Sessions", new ViewSessions(jTabbedPane));
        }else{
            JOptionPane.showMessageDialog(this, "Please select row!");
        }
    }//GEN-LAST:event_deleteSessionsBtnActionPerformed

    private void editSessionsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSessionsBtnActionPerformed
        // TODO add your handling code here:
        int row = sessions_jTable.getSelectedRow();

        if (row >= 0) {
            int sessionid = (int) sessions_jTable.getValueAt(row, 0);
            String lecturer = sessions_jTable.getValueAt(row, 1).toString();
            String subject = sessions_jTable.getValueAt(row, 2).toString();
            String tag = sessions_jTable.getValueAt(row, 3).toString();
            String groupid = sessions_jTable.getValueAt(row, 4).toString();
            int stuCount = (int) sessions_jTable.getValueAt(row, 5);
            int duration = (int) sessions_jTable.getValueAt(row, 6);

            Session session = new Session(lecturer, tag, groupid, subject, stuCount, duration);
            session.setSessionId(sessionid);
            
            jTabbedPane.remove(0);
            jTabbedPane.add("Edit Building Details", new AddEditSessions(session, jTabbedPane));
        }else{
            JOptionPane.showMessageDialog(this, "Please select row!");
        }

    }//GEN-LAST:event_editSessionsBtnActionPerformed


